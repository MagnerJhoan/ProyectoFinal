    // Método para el siguiente paso en la creación de cuenta
    public void siguientepaso(String tipoCuenta){
        boolean bolivianos = false; // Variable para indicar si se seleccionó la moneda boliviana
        boolean dolares = false; // Variable para indicar si se seleccionó la moneda dolar
        boolean moneda = false; // Variable para controlar la selección de moneda
        if (!checkBolivianos.isSelected() && !checkDolares.isSelected()){
            JOptionPane.showMessageDialog(null, "Ripuqpiypi kanman ñawi monedata t'ikrayta munani ", "llamk'ay", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Validación de selección de moneda
            if(checkBolivianos.isSelected() && !checkDolares.isSelected()){
                bolivianos = true;
                dolares = false;
                if (ahorro && !credito && !debito || !ahorro && credito && !debito || !ahorro && !credito && debito){
                    if(bolivianos && !dolares || !bolivianos && dolares){
                        Cuenta cuenta = new Cuenta (numerCuenta, "Bolivianos", 0, tipoCuenta, usuario); // Instancia de la clase Cuenta
                        principal.getListaCuenta().add(cuenta); // Agrega la cuenta a la lista principal
                        JOptionPane.showMessageDialog(null, "Yachaykama ripuq ñawinpi tukuyta tarinakuyta kamachi - Qullqi ripuqpi nispaqa: " + numerCuenta, "llaqtan", JOptionPane.INFORMATION_MESSAGE);
                        actualizarLista(); // Actualiza la lista de cuentas en el archivo
                        MenuQuechua ventana = new MenuQuechua(usuario, numerCuenta); // Instancia de la clase Menu
                        ventana.setVisible(true); // Muestra la ventana Menu
                        ventana.setLocationRelativeTo(null); // Centra la ventana en la pantalla
                        dispose(); // Cierra la ventana actual
                    }
                }
            } else if(!checkBolivianos.isSelected() && checkDolares.isSelected()){
                bolivianos = false;
                dolares = true;
                if (ahorro && !credito && !debito || !ahorro && credito && !debito || !ahorro && !credito && debito){
                    if(bolivianos && !dolares || !bolivianos && dolares){
                        Cuenta cuenta = new Cuenta (numerCuenta, "Dolares", 0, tipoCuenta, usuario); // Instancia de la clase Cuenta
                        principal.getListaCuenta().add(cuenta); // Agrega la cuenta a la lista principal
                        JOptionPane.showMessageDialog(null, "Yachaykama ripuq ñawinpi tukuyta tarinakuyta kamachi - Qullqi ripuqpi nispaqa: " + numerCuenta, "llaqtan", JOptionPane.INFORMATION_MESSAGE);
                        actualizarLista(); // Actualiza la lista de cuentas en el archivo
                        MenuQuechua ventana = new MenuQuechua(usuario, numerCuenta); // Instancia de la clase Menu
                        ventana.setVisible(true); // Muestra la ventana Menu
                        ventana.setLocationRelativeTo(null); // Centra la ventana en la pantalla
                        dispose(); // Cierra la ventana actual
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Kanman ripuq t'ikrayta munani, yachaykama.", "llamk'ay", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

    // Método para verificar la existencia de una cuenta dado un número de cuenta
    public boolean existe (int numerocuenta){
        boolean res = false ;
        for(int i =0; principal.getListaCuenta().size()>i;i++){
            if (principal.getListaCuenta().get(i).getNumeroCuenta() == numerocuenta){
                res = true ;
            }
        }
        return res;
    }
