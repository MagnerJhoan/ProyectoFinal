    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        Random random = new Random(); // Generador de números aleatorios
        this.numerCuenta = random.nextInt(200000000 - 100000000 + 1) + 100000000; // Genera un número de cuenta aleatorio
        boolean bandera = true; // Variable de control para asegurar la unicidad del número de cuenta
        while (bandera){
            if (!existe(numerCuenta)){ // Comprueba si el número de cuenta generado ya existe
                bandera = false; // Si no existe, se sale del bucle
            }
            this.numerCuenta = random.nextInt(200000000 - 100000000 + 1) + 100000000; // Genera otro número de cuenta aleatorio
        }

        // Validación de selección de tipo de cuenta y tipo de moneda
        if (!checkAhorro.isSelected() && !checkCredito.isSelected() && !checkDebito.isSelected()){
            JOptionPane.showMessageDialog(null, "Pisqaypa cuenta t'ikrayta munani, yachaykama ", "llamk'ay", JOptionPane.INFORMATION_MESSAGE);
        } else if (checkAhorro.isSelected() && !checkCredito.isSelected() && !checkDebito.isSelected()){
            ahorro = true;
            credito = false;
            debito = false;
            siguientepaso("Ahorro");
        } else if (!checkAhorro.isSelected() && checkCredito.isSelected() && !checkDebito.isSelected()){
            ahorro = false;
            credito = true;
            debito = false;
            siguientepaso("Credito");
        } else if (!checkAhorro.isSelected() && !checkCredito.isSelected() && checkDebito.isSelected()){
            ahorro = false;
            credito = false;
            debito = true;
            siguientepaso("debito");
        } else {
            JOptionPane.showMessageDialog(null, "Kanman ripuq t'ikrayta munani, yachaykama. ", "llamk'ay", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton5ActionPerformed
